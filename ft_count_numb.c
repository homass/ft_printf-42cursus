#include "ft_printf.h"

int	ft_count_numb(long int n)
{
	int	i;

	i = 1;
	while (1)
	{
		if (n < 0)
		{
			i++;
			n *= -1;
		}
		n = n / 10;
		if (n)
			i++;
		else
			break ;
	}
	return (i);
}
